# OR
L = [1,2,3]        # -literal notation

# 2. Creating a Dictionary
D = dict({'a': 1, 'b': 2, 'c': 3})
# OR
D = {'a': 1, 'b': 2, 'c': 3}

# 3. Creating a Tuple
